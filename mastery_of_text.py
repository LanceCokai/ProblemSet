import urllib.request
from collections import Counter
import re

url = 'https://www.gutenberg.org/cache/epub/10/pg10.txt'
response = urllib.request.urlopen(url)
pg10 = response.read().decode('utf-8')

lines = pg10.split('\n')
num_lines = len(lines)
print(f"Number of lines: {num_lines}")

words = re.findall(r'\b[a-zA-Z]+\b', pg10)
num_words = len(words)
print(f"Number of words: {num_words}")

word_count = Counter(word.lower() for word in words)
apostle_count = word_count['apostle']
print(f"Occurrences of the word 'apostle': {apostle_count}")

most_common_word, most_common_word_count = word_count.most_common(1)[0]
print(f"The most common word is '{most_common_word}' with {most_common_word_count} occurrences")

